using System;

class Program
{
    static void Main()
    {
        // Створюємо масив для зберігання людей
        int кількістьЛюдей = 3; // можеш змінити, якщо хочеш більше
        ЗнакЗодіаку[] люди = new ЗнакЗодіаку[кількістьЛюдей];

        // Вводимо дані від користувача
        for (int i = 0; i < кількістьЛюдей; i++)
        {
            Console.WriteLine($"\nВведіть дані для особи {i + 1}:");

            Console.Write("Введіть ПІБ: ");
            string піб = Console.ReadLine();

            Console.Write("Введіть дату народження (у форматі dd.MM.yyyy): ");
            string дата = Console.ReadLine();
            DateTime датаНародження = DateTime.Parse(дата);

            // Автоматично визначаємо знак зодіаку
            string знак = ВизначитиЗнакЗодіаку(датаНародження);

            // Додаємо людину до масиву
            ЗнакЗодіаку знакЗодіаку = new(піб, знак, дата);
            люди[i] = знакЗодіаку;
        }

        // Вводимо потрібний нам знак Зодіаку
        Console.Write("\nВведіть знак Зодіаку для пошуку: ");
        string шуканийЗнак = Console.ReadLine();

        // Виводимо всіх, хто має цей знак
        Console.WriteLine($"\nЛюди, народжені під знаком «{шуканийЗнак}»:");
        foreach (var людина in люди)
        {
            if (людина.GetЗнакЗодіаку().ToLower() == шуканийЗнак.ToLower())
            {
                Console.WriteLine($"{людина.ПІБ}, {людина.ДеньНародження:dd.MM.yyyy}");
            }
        }
    }

    static string ВизначитиЗнакЗодіаку(DateTime дата)
    {
        int місяць = дата.Month;
        int день = дата.Day;

        if ((місяць == 3 && день >= 21) || (місяць == 4 && день <= 19)) return "Овен";
        if ((місяць == 4 && день >= 20) || (місяць == 5 && день <= 20)) return "Телець";
        if ((місяць == 5 && день >= 21) || (місяць == 6 && день <= 20)) return "Близнюки";
        if ((місяць == 6 && день >= 21) || (місяць == 7 && день <= 22)) return "Рак";
        if ((місяць == 7 && день >= 23) || (місяць == 8 && день <= 22)) return "Лев";
        if ((місяць == 8 && день >= 23) || (місяць == 9 && день <= 22)) return "Терези";
        if ((місяць == 9 && день >= 23) || (місяць == 10 && день <= 22)) return "Скорпіон";
        if ((місяць == 10 && день >= 23) || (місяць == 11 && день <= 21)) return "Стрілець";
        if ((місяць == 11 && день >= 22) || (місяць == 12 && день <= 21)) return "Козеріг";
        if ((місяць == 12 && день >= 22) || (місяць == 1 && день <= 19)) return "Водолій";
        if ((місяць == 1 && день >= 20) || (місяць == 2 && день <= 18)) return "Риби";
        if ((місяць == 2 && день >= 19) || (місяць == 3 && день <= 20)) return "Овен";

        return "Невідомий знак";
    }
}
